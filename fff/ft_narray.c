#include "libft.h"

int		*ft_narray(char **str)
{
	size_t	i;
	size_t	j;
	size_t	k;
	int		*array;

	array = (int*)malloc(4 * 104);
	k = 0;
	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i][j] == '#')
			array[k++] = (((i % 4) + 1) * 10 + (j + 1));
		if (j == 3)
		{
			i++;
			j = 0;
		}
		else
			j++;
	}
	return (array);
}
